about_us_title = ''' About us '''

about_us_textbox1 = ''' 

Brazil is one of the largest producers of many agricultural commodities in the world. Every year, the Brazilian agriculture faces droughts or extreme weather events, leading to economic and resource losses. These events are expected to be more recurrent in the future and understanding the risks and potential adaptations are key to achieve food security. Representing drought or extreme events in agriculture is challenging due to the vast spectrum of crops and management strategies taking place at the same time (e.g. sugarcane can be less sensitive than soya or irrigation can offset dry spells). This challenge is amplified in continental countries such as Brazil, where a huge variety of ecosystems and culture is found all over the country. 

Nevertheless, research institutes and government agencies in Brazil (e.g. CONAB, ESALQ, Embrapa) often generates state-of-the-art projections of the impact of weather and climate conditions in agriculture. However, most of the projections are difficult to be applied on a daily-basis decision making, as they are generated for large scale areas (e.g. State/Country’s production) or are still in scientific publications. The National Centre for Monitoring and Early Warning of Natural Disasters (CEMADEN) makes use of an integrated system of remote sensed and ground data to issue hazard risk alerts via online bulletins and stakeholder meetings. Yet, we see opportunities to add robustness to risk assessment in Brazilian agriculture and to facilitate communication to a wider-public that can make use of regularly updated indices of extreme events for decision making.

Therefore, this project aims on improving the representation, monitoring and prediction of the climate impact and its risks in agricultural crops. Our partner scientists work hard on integrating information from the space (remote sensing) to the ground (weather stations 
and census) to better inform risks of drought and extreme events in crops.Simulation models are applied to understand the main crop responses to the environment that can be used to draw past and future projections and adaptation strategies. 

This web app was developed by Brazilian and UK scientists and programmers from the National Centre for Monitoring and Early Warning of Natural Disasters (CEMADEN) and the University of Leeds, which aims in providing underpinning information to understand the climate impacts and disaster risk in Brazilian agriculture. This tool is aimed to help decision making at municipal level (e.g. Civil Defense) and growers that can monitor the monthly drought conditions of their locations. A full description of indices is given in our About the tool page.

 '''

about_us_textbox2_title = ''' What is CSSP-Brazil? '''

about_us_textbox2_text = ''' 

The Climate Science for Service Partnership (CSSP) Brazil is a research programme that supports collaboration between the UK and Brazil, supported by the UK Government’s Newton Fund. 

It aims to develop capability to inform decision makers in climate mitigation and adaptation strategy and to underpin services to support climate and weather resilient economic development and social welfare. 

Our project is embedded within the CSSP-BR programme, and it's entitled: “Developing impacts modelling capability and understanding - Agricultural crop modelling and application”. The project is led by the ULeeds, with key partners such as Cemaden, Hadley Centre, UKCEH, Esalq/USP

 '''
 
