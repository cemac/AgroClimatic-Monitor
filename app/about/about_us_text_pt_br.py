about_us_textbox4_title = ''' Cemaden '''

about_us_textbox4_text = ''' 

Desde 2011, o Centro Nacional de Monitoramento e Alerta de Desastres Naturais (CEMADEN, 
http://www.cemaden.gov.br/), do Ministério da Ciência, Tecnologia e Inovação (MCTI) é 
responsável por atuar no nível federal de monitoramento e emissão de alertas para 
deslizamentos de terra, enchentes e inundações. Além disso, desde 2012, o Cemaden 
monitora operacionalmente as condições de seca e seus impactos em todo o Brasil. O 
sistema de monitoramento de secas do Cemaden identifica as áreas e municípios afetados, 
o que é uma ferramenta crucial para apoiar a gestão de risco. A caracterização dos 
eventos de seca (duração, severidade e extensão da área) também é considerada na 
avaliação dos impactos.

 '''
 
