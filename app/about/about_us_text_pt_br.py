about_us_title = ''' Sobre nós '''

about_us_textbox1 = ''' 

O Brasil é um dos maiores produtores de muitas commodities agrícolas do mundo. 
Todos os anos, a agricultura brasileira enfrenta secas ou eventos climáticos extremos, 
levando a perdas econômicas e de recursos. Espera-se que esses eventos sejam mais 
recorrentes no futuro e a compreensão dos riscos e possíveis adaptações são fundamentais 
para alcançar a segurança alimentar. No entanto, quantificar a seca ou eventos extremos 
na agricultura é um grande desafio devido ao vasto espectro de culturas e estratégias 
de manejo que ocorrem ao mesmo tempo (por exemplo, a cana-de-açúcar pode ser menos 
sensível do que a soja ou a irrigação pode compensar os períodos de seca). Esse desafio 
é ampliado em países continentais como o Brasil, onde uma grande variedade de 
ecossistemas e culturas é encontrada em todo o país.

Mesmo com esses desafios, institutos de pesquisa e agências governamentais no Brasil 
(por exemplo, CONAB, ESALQ, Embrapa) geralmente são capazes de gerar projeções no 
estado-da-arte sobre o impacto das condições meteorológicas e climáticas na agricultura. 
Ainda assim, a maioria das projeções são difíceis de serem aplicadas no dia-a-dia para 
tomada de decisão, pois são geradas para áreas de grande escala (por exemplo, produção 
do Estado/País) ou ainda estão em publicações científicas ou boletins técnicos. O 
Centro Nacional de Monitoramento e Alerta Antecipado de Desastres Naturais (CEMADEN) 
faz uso de um sistema integrado de sensoriamento remoto e dados terrestres para emitir 
alertas de risco de perigo por meio de boletins online e reuniões com as partes 
interessadas. Contudo, ainda vemos oportunidades para adicionar robustez à avaliação 
de risco na agricultura brasileira e para facilitar a comunicação para um público 
mais amplo que pode fazer uso de índices atualizados regularmente de eventos extremos 
para a tomada de decisão.

Portanto, este projeto visa melhorar a representação, o monitoramento e a previsão do 
impacto climático e seus riscos em culturas agrícolas. Nossos cientistas parceiros 
trabalham arduamente para integrar informações coletadas do espaço (sensoriamento remoto) 
ao solo (estações meteorológicas e censo) para melhor informar os riscos de seca e 
eventos extremos nas lavouras. Modelos de simulação são aplicados para entender as 
principais respostas da cultura ao ambiente, que podem ser usadas para entender padrões 
históricos e desenhar projeções futuras e estratégias de adaptação ao novo clima.

Este aplicativo da web foi desenvolvido por cientistas e programadores brasileiros 
e britânicos do Centro Nacional de Monitoramento e Alerta de Desastres Naturais 
(CEMADEN) e da Universidade de Leeds, que visa fornecer informações básicas para entender 
os impactos climáticos e o risco de desastres na agricultura brasileira. Esta ferramenta 
tem como objetivo auxiliar na tomada de decisões em nível municipal (por exemplo, Defesa 
Civil) e produtores que podem monitorar as condições de seca mensal de suas localidades. 
Uma descrição completa dos índices é fornecida em nossa página “Sobre a ferramenta”.

 '''

about_us_textbox2_title = ''' O que é CSSP-Brasil? '''

about_us_textbox2_text = ''' 

O Climate Science for Service Partnership (CSSP) Brasil é um programa de pesquisa 
que apóia a colaboração entre o Reino Unido e o Brasil, apoiado pelo Newton Fund do 
governo do Reino Unido.

Esse programa tem como objetivo desenvolver a capacitação e informar os tomadores de 
decisão sobre a mitigação climática e estratégia de adaptação e apoiar os serviços 
de apoio ao clima e ao desenvolvimento econômico resiliente às intempéries e ao 
bem-estar social.

Nosso projeto está inserido no programa CSSP-BR e é intitulado: “Desenvolvendo 
capacidade e compreensão de modelagem de impactos - Modelagem e aplicação de 
culturas agrícolas”. O projeto é liderado pela ULeeds, com parceiros importantes 
como Cemaden, Hadley Centre, UKCEH, Esalq / USP

 '''
 
